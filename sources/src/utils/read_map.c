#include "so_long.h"

void read_map(t_game *game)
{
	int i;
	int j;

	i = 0;
	while (i < game->map.rows)
	{
		j = 0;
		while (game->map.map[i][j])
		{
			printf(RED "r:%i, c:%i\n", i, j);
			if (game->map.map[i][j] == 'C')
				game->map.objets.goals += 1;
			j++;
		}
		i++;
	}
}
