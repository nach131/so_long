#include <stdio.h>

int main(void)
{

	int frame = 0;
	int i = 1;

	while (i < 4)
	{
		while (1)
		{
			if (!(frame % 100))
			{

				printf("loop: %d frame: %d\n", i, frame);
				break;
			}
			else
				frame++;
		}
		i++;
		frame = 1;
	}
}
